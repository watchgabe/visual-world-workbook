/** True if the key belongs to the content module (exact match or ig_/ca prefix) */
export function isCCKey(k: string): boolean {
  return !!(CC_EXACT.has(k) || /^(ig_|ca[123][a-z])/.test(k))
}

const IDEA_LETTERS = ['A', 'B', 'C', 'D']

/** Dynamic label for ig_ hierarchy keys */
export function igLabel(k: string): string {
  let m: RegExpMatchArray | null

  // ig_pillarN
  m = k.match(/^ig_pillar(\d)$/)
  if (m) return 'Content Pillar ' + m[1]

  // ig_pNiMaL — angle
  m = k.match(/^ig_p(\d)i(\d)a(\d)$/)
  if (m) return 'Pillar ' + m[1] + ' · Idea ' + IDEA_LETTERS[Number(m[2]) - 1] + ' · Angle ' + m[3]

  // ig_pNiM — idea
  m = k.match(/^ig_p(\d)i(\d)$/)
  if (m) return 'Pillar ' + m[1] + ' — Idea ' + IDEA_LETTERS[Number(m[2]) - 1]

  return k
}

/** Label for competitor analysis (ca) keys */
export function caLabel(k: string): string {
  const m = k.match(/^ca([123])(name|why|take|diff)$/)
  if (!m) return prettyKey(k)
  const map: Record<string, string> = {
    name: 'Name',
    why: 'Why Study',
    take: 'Key Takeaway',
    diff: "How I'm Different",
  }
  return 'Competitor ' + m[1] + ' — ' + (map[m[2]] || m[2])
}

const OPT_MAP: Record<string, string> = {
  genre: 'Brand Genre',
  style: 'Brand Style',
  vibe: 'Vibe',
  format: 'Format',
  audience: 'Audience',
  era: 'Era',
  energy: 'Energy',
  mbvibe: 'Mood Board Vibe',
}

/** Label for opt_ prefix keys */
export function optLabel(k: string): string {
  const g = k.replace('opt_', '')
  return OPT_MAP[g] || (g.charAt(0).toUpperCase() + g.slice(1))
}

/** Convert a camelCase/kebab-case key to a pretty title */
export function prettyKey(k: string): string {
  return k
    .replace(/[-_]/g, ' ')
    .replace(/([A-Z])/g, ' $1')
    .trim()
    .split(' ')
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ')
}

/** Get the best human-readable label for any field key */
export function getLabelForKey(k: string): string {
  if (LABELS[k]) return LABELS[k]
  if (k.startsWith('ig_')) return igLabel(k)
  if (/^ca[123][a-z]/.test(k)) return caLabel(k)
  if (k.startsWith('opt_')) return optLabel(k)
  return prettyKey(k)
}
